#include "header.h"

int	ft_putnbr(int n)
{
	char	c;
	int		tlen;

	tlen = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
		tlen++;
	}
	if (n >= 10)
		tlen += ft_putnbr(n / 10);
	c = n % 10 + '0';
	tlen += write(1, &c, 1);
	return (tlen);
}
